"""
Exercise: Print Hello, World

Problem:
Write a function called `hello_world` that takes no inputs and prints the message "Hello, World!".

Input:
- None

Output:
- The message "Hello, World!" printed to the screen.
"""


def hello_world():
    """
    Prints "Hello, World!" to the console.
    """
    print("Hello, World!")


def main():
    """
    Calls the hello_world function to display the message.
    """
    hello_world()


if __name__ == "__main__":
    main()
